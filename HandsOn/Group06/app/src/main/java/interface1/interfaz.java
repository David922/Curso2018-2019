        wifiPoint.setModel(modelo);
        String dis=distritos.getSelectedItem().toString();
        System.out.println(dis);
        
        distrito_titulo.setText(dis);
        
        ArrayList<WifiPoint> e=App.imprimir(dis);
        Iterator it= e.iterator();
